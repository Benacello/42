#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*dup;
	int		i;
	int		i2;

	i = 0;
	i2 = 0;
	while (src[i2] != '\0')
		i2++;
	dup = malloc(sizeof(*dup) * (i2 + 1));
	if (!dup)
		return (NULL);
	while (src[i] != '\0')
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
