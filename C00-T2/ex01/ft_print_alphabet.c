#include <unistd.h>

void	ft_putchar(char c)
{
	write (1, &c, 1);
}

void	ft_print_alphabet(void)
{
	char	al;

	al = 97;
	while (al < 123)
	{
		ft_putchar(al);
		al++;
	}	
}
